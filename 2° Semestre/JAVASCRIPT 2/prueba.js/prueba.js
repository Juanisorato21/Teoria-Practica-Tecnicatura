// Pedir al usuario que ingrese su nombre
var nombre = prompt("Ingrese su nombre:");

// Pedir al usuario que ingrese su apellido
var apellido = prompt("Ingrese su apellido:");

// Pedir al usuario que ingrese su correo electrónico
var email = prompt("Ingrese su correo electrónico:");

// Concatenar los datos ingresados por el usuario
var datosConcatenados = nombre + " " + apellido + " " + email;

// Imprimir los datos concatenados
console.log("Datos ingresados:", datosConcatenados);

